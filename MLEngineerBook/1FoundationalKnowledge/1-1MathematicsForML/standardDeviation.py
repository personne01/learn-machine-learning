import numpy as np

"""
    ===== StandardDeviation ===== 
    is a number that describes how spread out the values are
"""

nilai = [86,87,88,86,87,85,86]
s = np.std(nilai)
print(s)

"""
    ===== Variance ===== 
    is another number that indicates how spread out the values are
"""
v = np.var(nilai)
print(v)





